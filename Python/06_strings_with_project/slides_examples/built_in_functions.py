# examples of built-in Python functions and operators

print(len("Python"))         # outs 6
print(max('a', 'b', 'c'))    # outs c (done lexicographically)
print(max("air", "Apple"))   # outs air (lowercase is larger than uppercase)
print(min("python"))         # outs h (smallest letter)
print(max("python", "java")) # outs python
print("python" > "java")     # outs True
print("python" == "java")    # outs False
